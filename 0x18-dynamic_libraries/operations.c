
/**
 * add - adds two numbers.
 * @a: first number.
 * @b: second number.
 *
 * Return: Sum of a and b.
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two numbers.
 * @a: first number.
 * @b: second number.
 *
 * Return: Subtraction of b to a.
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two numbers.
 * @a: first number.
 * @b: second number.
 *
 * Return: Multiplication of a and b.
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers.
 * @a: first number.
 * @b: second number.
 *
 * Return: Divition of a and b.
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - module of division a and b.
 * @a: first number.
 * @b: second number.
 *
 * Return: Module of a and b.
 */
int mod(int a, int b)
{
	return (a % b);
}
